#include "3-calc.h"

/**
 * op_add - add two int
 * @a: first int
 * @b: second int
 * Return: add
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub two int
 * @a: first int
 * @b: second int
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul two int
 * @a: first int
 * @b: second int
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div two int
 * @a: first int
 * @b: second int
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module of two int
 * @a: first int
 * @b: second int
 * Return: module
 */
int op_mod(int a, int b)
{
	return (a % b);
}
